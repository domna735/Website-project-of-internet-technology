        <p>This shows that ChatGPT might fail to identify the authenticity of the data correctly. Furthermore, it is undeniable that ChatGPT sometimes wrongly understands the tone and content of the users' speech, despite having an excellent natural language understanding ability. And for more subtle emotions, ChatGPT is not different from other chatbots, so it cannot cope with emotional support in customer service. Likewise, ChatGPT's self-censorship feature is not immaculate. There are several cases where it will fall to user deception and provide improper responses and prejudiced remarks. This phenomenon will undoubtedly have an immeasurable impact on users as ChatGPT has no age limit. Similarly, AI-based apps can be used maliciously by skilled cybercriminals. Although the creative creation of ChatGPT seems to be its product, it still relies on human speech or text. If users use it unrestrainedly, it will cause trouble. For example, users may infringe the copyright of the actual creator by using ChatGPT. This effect, in disguise, has hit the creative market's return rate and creators' confidence.</p>
